COMMENT ON COLUMN APP_JADE_CP.CP_CLS       IS 'CP종류 1:온라인 모바일직가맹, 2:온라인 모바일영업대행';
COMMENT ON COLUMN APP_JADE_CP.START_DT     IS '개통일자';
COMMENT ON COLUMN APP_JADE_CP.END_DT       IS '종료일자';
COMMENT ON COLUMN APP_JADE_CP.CONFIRM      IS '0:서비스중지, 1:CP요청상태, 2:사업부 승인 상태, 3:관리부서 최종 승인';
