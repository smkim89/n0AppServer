DROP TABLE APP_JADE_CP CASCADE CONSTRAINT PURGE;
CREATE TABLE APP_JADE_CP (
	AGENT_ID	VARCHAR2(20) PRIMARY KEY,
	CP_ID		VARCHAR2(20) NOT NULL,
	CP_NM		VARCHAR2(50),
	CP_CLS		VARCHAR2(1)	 NOT NULL,
	START_DT	VARCHAR2(8),
	END_DT		VARCHAR2(8),
	CONFIRM		VARCHAR2(1)	 NOT NULL,
	REG_DH		VARCHAR2(14),
	REG_ID		VARCHAR2(20),
	UPT_DH		VARCHAR2(14),
	UPT_ID		VARCHAR2(20),
	PG_ID		VARCHAR2(8),
	PG_NM		VARCHAR2(50),
	COM_CD		VARCHAR2(10),
	KSCC_TC_ID	VARCHAR2(9),
	SETTLE_YN	CHAR(1),
	SYNC_URL	VARCHAR2(100),
	CRYPTO_KEY	VARCHAR2(64)
);

CREATE INDEX IDX_JADE_CP_ID ON APP_JADE_CP('CP_ID');
CREATE INDEX IDX_JADE_CP_PG_ID ON APP_JADE_CP('PG_ID');
CREATE INDEX IDX_JADE_CP_COM_CD ON APP_JADE_CP('COM_CD');

